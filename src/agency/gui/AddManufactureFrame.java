    private void btnAddManufacturerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddManufacturerActionPerformed
        int conf = JOptionPane.showConfirmDialog(null, "Are you sure to "
                + "add Construction " + txtManufacturerName.getText() + "?", "Confirm",
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (conf == 0) {
            try {
                Manufacturer manufacturer = new Manufacturer();
                manufacturer.setName(txtManufacturerName.getText());
                manufacturer.setManuid(stripOutID(txtManufacturerID.getText()));
                mjc.create(manufacturer);

                conf = JOptionPane.showConfirmDialog(null, "Manufacturer was added "
                        + "successfully\n Continue further adding ?", "Successful",
                        JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
                if (conf == 0) {
                    refreshFields();
                    clearFields();
                } else {
                    this.dispose();
                }
            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null, "Error occured in adding .\n"
                        + ex.getMessage(), "ClassNotFound Error", JOptionPane.ERROR_MESSAGE);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Error occured in adding .\n"
                        + ex.getMessage(), "Remote SQL Error", JOptionPane.ERROR_MESSAGE);
            }
        }
//        System.gc();
    }//GEN-LAST:event_btnAddManufacturerActionPerformed

