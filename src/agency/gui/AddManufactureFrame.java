    private void refreshFields() {
        txtManufacturerID.setText(getManuID(mjc.getManufacturerCount(), 8));
    }

    private String getManuID(int count, int length) {
        return String.format("M" + "%0" + length + "d", ++count);
    }

    private Long stripOutID(String text) {
        return Long.parseLong(text.substring(1));
    }

    private void clearFields() {
        txtManufacturerName.setText("");
    }
