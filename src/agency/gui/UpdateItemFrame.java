    private void btnUpdateSupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateSupplierActionPerformed
        int conf = JOptionPane.showConfirmDialog(null, "Are sure to update these values?",
                "Confirm", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (conf == 0) {
            try {
                Long itemID = OtherController.stripOutItemCode(itemCode);
                Item item = ijc.findItem(itemID);

                Metric metric = cjc.getConstructionForName((String) cmbMetric.getSelectedItem());
                Manufacturer manufacturer = mjc.findManufacturerForName((String) cmbManufacturer.getSelectedItem());
                Category category = (Category) cmbCategory.getSelectedItem();

                item.setMetric(metric);
                item.setManufacturer(manufacturer);
                item.setName(txtItemName.getText());
                item.setCategory(category);

                ijc.edit(item);
                JOptionPane.showMessageDialog(null, "Sucessfully Updated \n"
                        + item.getName(), "Successful", JOptionPane.INFORMATION_MESSAGE);
                ijc.setChanged();
                ijc.notifyObservers("addItem");
        //        System.gc();
                this.dispose();
        //        System.gc();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error occured in\n"
                        + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
             //   Logger.getLogger(UpdateItemFrame.class.getName()).log(Level.SEVERE, null, e);
        //        System.gc();
            }
        }


    }//GEN-LAST:event_btnUpdateSupplierActionPerformed

