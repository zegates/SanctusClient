    private void refreshConstructions() {
        cmbMetric.removeAllItems();
        List<Metric> constructions = cjc.findConstructionEntities();
        for (Metric construction : constructions) {
            cmbMetric.addItem(construction.getName());
        }
//        System.gc();
    }

    private void refreshManufacturers() {
        cmbManufacturer.removeAllItems();
        List<Manufacturer> manufacturers = mjc.findManufacturerEntities();
        Collections.sort(manufacturers);
        for (Manufacturer manufacturer : manufacturers) {
            cmbManufacturer.addItem(manufacturer.getName());
        }
//        System.gc();
    }

   

    private void refreshCategories() {
        cmbCategory.removeAllItems();
//        System.gc();
    }

    private void evaluateValues() {
        Long itemID = OtherController.stripOutItemCode(itemCode);
        Item item = ijc.findItem(itemID);
        if (item == null) {
            JOptionPane.showMessageDialog(null, "Error occured in\n selecting item"
                    + "from the database", "Error", JOptionPane.ERROR_MESSAGE);
            this.dispose();
        }

        txtItemCode.setText(OtherController.formatCode("I", item.getIid(), 8));
        txtItemName.setText(item.getName());
        cmbCategory.setSelectedItem(item.getCategory());
        cmbMetric.setSelectedItem(item.getMetric().getName());
        cmbManufacturer.setSelectedItem(item.getManufacturer().getName());
        
//        System.gc();
    }
