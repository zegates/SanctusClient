    private void btnUpdateDetailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateDetailActionPerformed
        try {

            String itemName = (String) cmbItemName.getSelectedItem();
            String[] split = itemName.split(" -- ");
            Item item = ijc.findItem(Long.parseLong(split[0]));
            SupplyOrderDetail detail = sodjc.findSupplyOrderDetail(Long.parseLong(txtDeatialID.getText()));
            if (detail != null) {
                //detail.setSodid();
                detail.setItem(item);
                detail.setQty(Integer.parseInt(txtQuantity.getText()));
                detail.setRemainingQty(Integer.parseInt(txtRQuantity.getText()));
                detail.setBuyingPrice(Double.parseDouble(txtBuyingprice.getText()));
                detail.setSellingPrice(Double.parseDouble(txtSellingPrice.getText()));
                int conf = JOptionPane.showConfirmDialog(null, "are you sure these values "
                        + "are correct?", "Confirm", JOptionPane.YES_NO_OPTION,
                        JOptionPane.QUESTION_MESSAGE);
                if (conf == 0) {
                    sodjc.edit(detail);
                    JOptionPane.showMessageDialog(this, "Order Detail Updated Successfully", "Update Record", JOptionPane.INFORMATION_MESSAGE);
                    setId();
                }
            } else {
        //        System.gc();
                throw new Exception("Supply Detail was null.. ");
            }
        } catch (NonexistentEntityException ex) {
            JOptionPane.showMessageDialog(this, "Error in updating "
                    + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
//            Logger.getLogger(UpdateSupplyOrderDetail.class.getName()).log(Level.SEVERE, null, ex);
    //        System.gc();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error in updating "
                    + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            //Logger.getLogger(UpdateSupplyOrderDetail.class.getName()).log(Level.SEVERE, null, ex);
    //        System.gc();
        }
//        System.gc();
    }//GEN-LAST:event_btnUpdateDetailActionPerformed

