    private void validation() {
        if (cmbItemName.getSelectedIndex() != -1 && !"".equals(txtRQuantity.getText()) && !"".equals(txtQuantity.getText()) && !"".equals(txtBuyingprice.getText()) && !"".equals(txtSellingPrice.getText())) {
            btnUpdateDetail.setEnabled(true);
        } else {
            btnUpdateDetail.setEnabled(false);
        }
    }

    private void setId() {
        List<Item> items = ijc.findItemEntities();
        String selected = null;
        cmbItemName.removeAllItems();
        for (Item item : items) {
            String itemName = item.getIid() + " -- " + item.getManufacturer().getName() + " " + item.getName();
            if (item.getIid() == iid) {
                selected = itemName;
            }
            cmbItemName.addItem(itemName);
        }
        if (selected != null) {
            cmbItemName.setSelectedItem(selected);
        }
    }
