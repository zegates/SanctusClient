    private void btnDeleteOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteOrderActionPerformed
        Orders orders = ojc.findOrders(Long.parseLong(txtID.getText()));
        if (orders != null) {
            try {
                int conf = JOptionPane.showConfirmDialog(null, "Do you really want to delete this order?", "Confirm",
                        JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

                if (conf == 0) {
                    List<OrderDetail> orderDetails = orders.getOrderDetails();
                    for (int i = 0; i < orderDetails.size(); i++) {
                        OrderDetail orderDetail = orderDetails.get(i);
                        SupplyOrderDetail supplyOrderDetail = orderDetail.getSupplyOrderDetail();
                        supplyOrderDetail.setRemainingQty(supplyOrderDetail.getRemainingQty() + orderDetail.getQty());
                        sodjc.edit(supplyOrderDetail);
                    }

                    ojc.destroy(orders.getOid());

                    JOptionPane.showMessageDialog(this, "Successfully Deleted", "Successful", JOptionPane.INFORMATION_MESSAGE);
                    ojc.setChanged();
                    ojc.notifyObservers("addOrder");
                    this.dispose();
                }
            } catch (NonexistentEntityException ex) {
                JOptionPane.showMessageDialog(this, "Error with the order informations\n"
                        + "Couldnot perform the operation\n" + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Error with the order\n"
                        + "\n" + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        //        System.gc();
            }
        }
//        System.gc();
    }//GEN-LAST:event_btnDeleteOrderActionPerformed

    private void btnPrintOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrintOrderActionPerformed
        JRTableModelDataSource jrt = new JRTableModelDataSource(dtm);
        String repSource = "./src/tireshop/reports/OrderPosBill.jrxml";
        Map<String, Object> param = new HashMap<String, Object>();


        param.put("Date", txtDate.getText());
        param.put("CustName", txtName.getText());
        param.put("Time", txtTime.getText());
        try {
            param.put("Orderid",
                    OtherController.formatCode("OD", Long.parseLong(txtID.getText()), 8));
            param.put("Total", txtTotalBill.getText());

            param.put("Discount", txtDiscount.getText());
            double ttbill = Double.parseDouble(txtTotalBill.getText());
            double ttDue = Double.parseDouble(txtDue.getText());

            param.put("Paid", OtherController.formatPrice(ttbill - ttDue));
            param.put("Balance", txtDue.getText());

            param.put("VehicleNo", "**********");

            JasperReport jr;
            try {
                jr = JasperCompileManager.compileReport(repSource);
                JasperPrint jp = JasperFillManager.fillReport(jr, param, jrt);

                //JasperViewer.viewReport(jp, false);
                try {
                    JasperPrintManager.printReport(jp, false);
                } catch (JRException ee) {
                    JOptionPane.showMessageDialog(null, "Printer not found ",
                            "Printer Error", JOptionPane.ERROR_MESSAGE);
                    JasperViewer.viewReport(jp, false);
                } catch (Exception ee) {
                    JOptionPane.showMessageDialog(null, "Cannot be prited ",
                            "Printer Error", JOptionPane.ERROR_MESSAGE);
                }

            } catch (JRException ex) {
                Logger.getLogger(ViewOrder.class.getName()).log(Level.SEVERE, null, ex);
            }

        } catch (NumberFormatException s) {
        }
    }//GEN-LAST:event_btnPrintOrderActionPerformed

