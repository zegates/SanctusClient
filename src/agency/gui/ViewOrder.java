    private void txtIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtIDActionPerformed
        String idTxt = txtID.getText();
        if (!"".equals(idTxt)) {
            try {
                long id = Long.parseLong(idTxt);
                Orders found = ojc.findOrders(id);
                System.out.println("awa");
                if (found != null) {
                    txtDate.setText(found.getDateAdded().toString());
                    txtTime.setText(found.getTimeAdded().toString());
                    txtName.setText(found.getCustName());
                    txtAddres.setText(found.getAddress());
                    txtTPNo.setText(found.getTpNo());
                    txtTotal.setText(found.getTotal() + "");
                    txtDiscount.setText(found.getDiscount() + "");
                    txtTotalBill.setText((found.getTotal() - found.getDiscount()) + "");
                    txtDue.setText((found.getTotal() - found.getPaidAmount() - found.getDiscount()) + "");
                    List<OrderDetail> orderDetails = found.getOrderDetails();
                    dtm.setRowCount(0);
                    for (OrderDetail detail : orderDetails) {
                        dtm.addRow(new Object[]{detail.getSupplyOrderDetail().getItem().getIid(), detail.getSupplyOrderDetail().getItem().getName(), detail.getSupplyOrderDetail().getSupplyOrder().getSoid(), detail.getQty(), detail.getUnitPrice(), detail.getQty() * detail.getUnitPrice()});
                    }
                    if (found.isPaid()) {
                        btnDoPayment.setEnabled(false);
                    } else {
                        btnDoPayment.setEnabled(true);
                    }
                    btnDeleteOrder.setEnabled(true);
                    btnPrintOrder.setEnabled(true);
                } else {
                    JOptionPane.showMessageDialog(this, "Please enter valid Order ID");
                    btnDeleteOrder.setEnabled(false);
                }
            } catch (NumberFormatException ex) {
                txtID.setText("");
                JOptionPane.showMessageDialog(this, "Please enter valid Order ID");
                btnDeleteOrder.setEnabled(false);
            }
        }
//        System.gc();
    }//GEN-LAST:event_txtIDActionPerformed

